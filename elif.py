marks = int(input("what is your score ? "))
if marks >= 80:
    print("A")
elif marks >= 75:
    print("A-")
elif marks >= 70:
    print("B+")
elif marks >= 65:
    print("B")
elif marks >= 60:
    print("B-")
elif marks >= 55:
    print("C+")
elif marks >= 50:
    print("C")
elif marks >= 45:
    print("C-")
elif marks >= 40:
    print("D+")
elif marks >= 35:
    print("D")
elif marks >= 30:
    print("D-")
else:
    print("E")
